/**
 * Popup placement types.
 *
 * @public
 */
enum Placement {
  /**
   * Popup will be placed at the left side of the reference element.
   * @public
   */
  Left = 'Left',

  /**
   * Popup will be placed at the right side of the reference element.
   * @public
   */
  Right = 'Right',

  /**
   * Popup will be placed at the top of the reference element.
   * @public
   */
  Top = 'Top',

  /**
   * Popup will be placed at the bottom of the reference element.
   * @public
   */
  Bottom = 'Bottom',
}

export default Placement;
